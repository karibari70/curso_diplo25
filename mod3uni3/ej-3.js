//  Creo un array con productos que necesito comprar
let productos = ["pan", "leche", "arroz", "jabón", "frutas"]; // Lista de productos

//   bucle for para recorrer el array
for (let i = 0; i < productos.length; i++) {
  
  let item = productos[i];

  // Mostrar el mensaje en consola 
  console.log(`¡No te olvides de comprar ${item}, por favor!`);
}
